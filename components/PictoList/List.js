const populateLangsOnIcons = function (iconList) {
  for (var iconItem in iconList) {
    if (iconList.hasOwnProperty(iconItem)) {
      for (var i = 0; i < jsonList.length; i++) {
        if ((iconItem + '.png') === jsonList[i].filename) {
          iconList[iconItem].langs = {
            fr: jsonList[i].french,
            en: jsonList[i].english,
            sl: jsonList[i].slovenian,
            ar: jsonList[i].arabic,
            et: jsonList[i].estonian,
            es: jsonList[i].spanish,
            pt: jsonList[i].portuguese,
            sr: jsonList[i].serbian,
            it: jsonList[i].italian,
            de: jsonList[i].german,
            zh: jsonList[i].mandarin,
            el: jsonList[i].greek,
            hi: jsonList[i].hindi,
            pa: jsonList[i]['lahnda/pendjabi'],
            ru: jsonList[i].russian,
            id: jsonList[i].indonesian,
            bn: jsonList[i].bengali
          };
        }
      }
    }
  }

  return iconList;
};

